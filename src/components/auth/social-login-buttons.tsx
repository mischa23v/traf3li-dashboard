interface SocialLoginButtonsProps {
  onLogin?: (provider: OAuthProvider) => void
  loading?: boolean
  disabled?: boolean
  returnUrl?: string
  providers?: OAuthProvider[]
  layout?: 'horizontal' | 'vertical' | 'grid'
  showLabels?: boolean
  showDivider?: boolean
  dividerText?: string
  className?: string
}

export function SocialLoginButtons({
  onLogin,
  loading = false,
  disabled = false,
  returnUrl = '/dashboard',
  providers: providersFilter,
  layout = 'vertical',
  showLabels = true,
  showDivider = true,
  dividerText,
  className,
}: SocialLoginButtonsProps) {
  const { t, i18n } = useTranslation()
  const isRTL = i18n.dir() === 'rtl'
  const oauthLogin = useOAuthLogin()
  const { data: availableProviders = OAUTH_PROVIDERS } = useAvailableProviders()

  // Filter providers if specified
  const providers = providersFilter
    ? availableProviders.filter((p) => providersFilter.includes(p.id))
    : availableProviders

  const handleLogin = async (provider: OAuthProvider) => {
    if (onLogin) {
      onLogin(provider)
    } else {
      oauthLogin.mutate({ provider, returnUrl })
    }
  }

  const isLoading = loading || oauthLogin.isPending

  return (
    <div className={cn('space-y-4', className)}>
      {showDivider && (
        <div className="relative">
          <div className="absolute inset-0 flex items-center">
            <Separator className="w-full" />
          </div>
          <div className="relative flex justify-center text-xs uppercase">
            <span className="bg-background px-2 text-muted-foreground">
              {dividerText || t('auth.orContinueWith', 'أو تسجيل الدخول عبر')}
            </span>
          </div>
        </div>
      )}

      <div
        className={cn(
          'flex gap-3',
          layout === 'vertical' && 'flex-col',
          layout === 'horizontal' && 'flex-row flex-wrap justify-center',
          layout === 'grid' && 'grid grid-cols-2 sm:grid-cols-3'
        )}
      >
        {providers.map((provider) => {
          const Icon = ProviderIcons[provider.id]
          const name = isRTL ? provider.nameAr : provider.name

          return (
            <Button
              key={provider.id}
              variant="outline"
              type="button"
              disabled={disabled || isLoading || !provider.available}
              onClick={() => handleLogin(provider.id)}
              className={cn(
                'relative',
                layout === 'vertical' && 'w-full',
                layout === 'horizontal' && 'flex-1 min-w-[120px]'
              )}
            >
              <Icon className="h-5 w-5" />
              {showLabels && <span className="mr-2">{name}</span>}
            </Button>
          )
        })}
      </div>
    </div>
  )
}

export default SocialLoginButtons
